public class DataStruc2 {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        int num = 5;
        
        for(int x = 0; x < num; num--)
        {
            for(int y =0; y < num; num--)
            {
                System.out.print(num);
            }
            System.out.println();
        }
    }
    
}
